				<div class="col-md-12  animate-box">
					<h3>Logros y Reconocimientos</h3>
				La década de los 90 estuvo marcada por grandes cambios para la escuela.  En 1998, desfiló la trama "Un viaje a la Atlántida", y consiguió el título de campeón del Grupo 2 del Carnaval de Sao Paulo.

				<h3> 2001</h3>
				<p>Con la trama "Vila Maria: después las escenas de los próximos capítulos", que cuenta la historia y la influencia de las novelas en la vida cotidiana brasilera, la escuela se convierte en campeona del grupo de acceso, pasando a integrar la elite del carnaval paulista y figurar entre las escuelas de mayor notoriedad en el país.
				</p>

				<h3>2010</h3>
				<p>La escuela presentó la trama "La industria que manipula el hierro, es la madre de todas las demás”, de autoría del carnavalesco Fábio Borges. Sus grandes proporciones alcanzó el límite máximo de altura del Sambódromo del Anhembi.</p>

				<h3>2011</h3>
				<p><b>"Teatro Amazonas - Manaus en el escenario"</b>, escrita por Fabio Borges, logró el tercer lugar detrás de la sorprendente Tucuruvi y de la campeona Vai-Vai.</p>

				<h3>2012</h3>
				<p>La escuela desfiló 'La Fuerza Infinita de la Creación - Vila Maria Feada a Mano', logrando el quinto lugar, a cargo del carnavalesco experimentado Chico Spinosa.</p>

				<h3>2013</h3>
				<p>La trama giró en torno al 50 aniversario de la inmigración coreana en Brasil. El desfile fue muy lujoso y la escuela fue cotizada como una de las favoritas.</p>

				<h3>2014</h3>
				<p>Vila María mostró la historia de los juguetes "En mis 60 años de alegría - Soy Vila Maria, y hago la fiesta rescatando del pasado juguetes del tiempo de niño.” Fue la campeona del grupo de acceso, obteniendo la puntuación máxima de 270 puntos, volviendo así al grupo especial del carnaval en 2015.
				</p>

				<h3>2015</h3>
				<p>Se desarrolló la trama "¡Sólo los diamantes son eternos en la química divina!" del carnavalesco Lucas Pinto. La escuela terminó en el 10 ° lugar, permaneciendo en la elite del carnaval paulista.</p>

				<h3>2016</h3>
				<p>Se honró la isla de Ilhabela con el tema: "El pueblo famoso es más Bela, Ilhabela fantasía". Hizo un desfile sofisticado, técnico e impecable garantizando un 5º Lugar.</p>

				<h3>2017</h3>
				<p>La escuela desfiló con el tema del tercer centenario de la estatua de la aparición de la Virgen en el río Paraíba. Con el respaldo de la iglesia la escuela se comprometió a cumplir con requisitos como: no permitir la desnudez, no hablar del sincretismo religioso y permitir la supervisión de la Arquidiócesis de San Pablo durante todo el proceso. El tema fue muy bien recibido por la comunidad y la gran mayoría de los devotos obteniendo el 7º lugar en la clasificación general.</p>

				<h3>2018</h3>
				<p>Representó la cultura mexicana, con personajes mayas y aztecas, el arte de Frida Kahlo, rindió un homenaje a Roberto Bolaños y personajes como el Chavo y el Chapulin Colorado.</p>

				<h3>2019</h3>
				<p>La escuela homenajeará al Perú con el tema "En las alas del gran pájaro, el vuelo de Vila María al Imperio del Sol". Para el desarrollo de la historia la escuela trajo de vuelta al carnavalesco Alexandre Louzada y a Cristiano Bara.</p>



